#Write a function that takes a category and computes the average IMDB score.
def average_in_category(movies, c):
    total=0.0
    filtered=[m for m in movies if m["category"]==c]

    for m in filtered:
        total+=m["imdb"]
    
    if len(filtered)>0:
        avr=total/len(filtered)
        return avr
    else:
        return 0.0


